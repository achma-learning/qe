#!/usr/bin/env node
'use strict';
/**
 * Build the high-yield analysis manifest for high-yield.html.
 *
 * GitHub Pages can't list a directory at runtime, so we scan qe-analysis/<slug>/
 * for uploaded documents (PDF/Word) and bake the result into
 * qe-analysis/_analysis.js as `window.QE_ANALYSIS = { "<slug>": ["file.pdf"] }`.
 *
 * Also (idempotently) creates an empty qe-analysis/<slug>/ folder for every
 * module, so there's always a place to drop a file.
 *
 * Usage:  node tools/build-analysis.js   (also run automatically in CI on push)
 */
const fs = require('fs');
const path = require('path');
const { ROOT, loadModules } = require('./parser-bridge');

const DIR = path.join(ROOT, 'qe-analysis');
const DOC_RE = /\.(pdf|docx?|odt|pptx?|rtf|md|txt)$/i;       // accepted document types
const isDoc = (f) => DOC_RE.test(f) && !f.startsWith('.');

function main() {
  const modules = loadModules();
  fs.mkdirSync(DIR, { recursive: true });

  const manifest = {};
  let withDocs = 0, totalDocs = 0;
  for (const m of modules) {
    const dir = path.join(DIR, m.slug);
    if (!fs.existsSync(dir)) {
      fs.mkdirSync(dir, { recursive: true });
      fs.writeFileSync(path.join(dir, '.gitkeep'), '');     // keep the empty folder in git
    }
    const files = fs.readdirSync(dir).filter(isDoc).sort();
    if (files.length) { manifest[m.slug] = files; withDocs++; totalDocs += files.length; }
    console.log(`${files.length ? '📄' : ' ·'} ${m.slug.padEnd(36)} ${files.length} doc(s)`);
  }

  const js = `/* auto-generated by tools/build-analysis.js — do not edit */\nwindow.QE_ANALYSIS = ${JSON.stringify(manifest)};\n`;
  fs.writeFileSync(path.join(DIR, '_analysis.js'), js);
  console.log(`\nWrote qe-analysis/_analysis.js — ${withDocs}/${modules.length} module(s) with analysis · ${totalDocs} document(s)`);
}

main();
