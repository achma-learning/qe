#!/usr/bin/env node
/* Bakes the curriculum outlines under `data/liste cours/` into
 * `data/liste cours/_curriculum.js` (window.QE_CURRICULUM), the same way
 * tools/build-data.js bakes the question .txt → data/<slug>.data.js. The
 * curriculum page (curriculum.html → QE.bootCurriculum) parses the raw outline
 * client-side; this baker only embeds the raw text and records which files
 * exist, so the page renders offline (file://) without a fetch.
 *
 * Run by CI alongside build-data.js / build-analysis.js. Do NOT hand-edit the
 * output — edit the source `.txt` files and re-run this.
 *
 *   node tools/build-curriculum.js
 */
'use strict';
const fs = require('fs');
const path = require('path');

const DIR_REL = 'data/liste cours';
const DIR = path.join(__dirname, '..', DIR_REL);
const SEMS = ['s1', 's2', 's3', 's4', 's5', 's6', 's7', 's8', 's9', 's10'];
const COMPLETE = 'curriculum_FMPM_S1-S10.txt';

function readIf(file) {
  try { return fs.readFileSync(path.join(DIR, file), 'utf8'); } catch { return null; }
}

const semesters = {};
let present = 0;
for (const s of SEMS) {
  const file = `${s} liste cours.txt`;
  const raw = readIf(file);
  const exists = raw !== null;
  if (exists) present++;
  semesters[s] = { file, exists, raw: exists ? raw : '' };
}
const completeRaw = readIf(COMPLETE);

const payload = {
  dir: DIR_REL + '/',
  complete: { file: COMPLETE, exists: completeRaw !== null },
  semesters,
};

fs.mkdirSync(DIR, { recursive: true });
const out = '/* auto-generated by tools/build-curriculum.js — do not edit */\n'
  + 'window.QE_CURRICULUM = ' + JSON.stringify(payload) + ';\n';
fs.writeFileSync(path.join(DIR, '_curriculum.js'), out);

const missing = SEMS.filter(s => !semesters[s].exists);
console.log(`Wrote ${DIR_REL}/_curriculum.js — ${present}/${SEMS.length} semester file(s)`
  + `${payload.complete.exists ? ' + complete' : ' (complete file missing)'}`
  + (missing.length ? ` · missing: ${missing.join(', ')}` : ''));
